import fs from 'node:fs';
import path from 'node:path';

const repoRoot = process.cwd();
const webRoot = path.join(repoRoot, 'src/web');
const indexPath = path.join(webRoot, 'index.html');

function collectJs(dir, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) collectJs(full, out);
    else if (ent.isFile() && ent.name.endsWith('.js')) out.push(full);
  }
  return out;
}

const jsFiles = collectJs(webRoot);
const html = fs.existsSync(indexPath) ? fs.readFileSync(indexPath, 'utf8') : '';

const includeRe = /<script\s+defer\s+src="([^"]+)"\s*><\/script>/g;
const localIncludes = [];
let m;
while ((m = includeRe.exec(html)) !== null) {
  if (!/^https?:\/\//.test(m[1])) localIncludes.push(m[1].split('?')[0]);
}

const inlineRe = /\son[a-z]+\s*=\s*"([^"]+)"/gi;
let inlineCount = 0;
while (inlineRe.exec(html) !== null) inlineCount += 1;

console.log('# Web Check Report');
console.log('');
console.log(`- JS files under src/web: **${jsFiles.length}**`);
console.log(`- Local deferred script includes in src/web/index.html: **${localIncludes.length}**`);
console.log(`- Inline handlers in src/web/index.html: **${inlineCount}**`);
console.log('');
console.log('Generated by `node scripts/check-web-report.mjs`.');
